package kr.or.ddit.friend.vo;

public class FriendVO {

	private String friendId;
	private String memId;
	private String friendDate;
	private String friendNo;
	
	public String getFriendId() {
		return friendId;
	}
	public void setFriendId(String friendId) {
		this.friendId = friendId;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getFriendDate() {
		return friendDate;
	}
	public void setFriendDate(String friendDate) {
		this.friendDate = friendDate;
	}
	public String getFriendNo() {
		return friendNo;
	}
	public void setFriendNo(String friendNo) {
		this.friendNo = friendNo;
	}
	
	
	
}
